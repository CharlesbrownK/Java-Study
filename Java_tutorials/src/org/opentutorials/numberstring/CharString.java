package org.opentutorials.numberstring;

public class CharString {

	public static void main(String[] args) {
		
		System.out.println("Chk"); // String
		System.out.println("Charlesbrown k"); // String
		System.out.println("Hello! I'm " + "Charlesbrown k"); // String
		System.out.println("Charlesbrown k says \"Welcome to the Java World\""); // String in double quotation marks
		System.out.println("HTML5\nCSS\nJava\nPython\nAnd So On"); // Wrap in a string

	}

}
